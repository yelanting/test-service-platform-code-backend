package com.testservice.platform.server.vo;

/**
 * 登录接口封装对象
 * 
 * @author administrator
 * @date Oct 29, 2018
 */
public class LoginBean {

	private String account;
	private String password;
	private String captcha;

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCaptcha() {
		return captcha;
	}

	public void setCaptcha(String captcha) {
		this.captcha = captcha;
	}

	@Override
	public String toString() {
		return "LoginBean [account=" + account + ", password=" + password
		        + ", captcha=" + captcha + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((account == null) ? 0 : account.hashCode());
		result = prime * result + ((captcha == null) ? 0 : captcha.hashCode());
		result = prime * result
		        + ((password == null) ? 0 : password.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		LoginBean other = (LoginBean) obj;
		if (account == null) {
			if (other.account != null) {
				return false;
			}
		} else if (!account.equals(other.account)) {
			return false;
		}
		if (captcha == null) {
			if (other.captcha != null) {
				return false;
			}
		} else if (!captcha.equals(other.captcha)) {
			return false;
		}
		if (password == null) {
			if (other.password != null) {
				return false;
			}
		} else if (!password.equals(other.password)) {
			return false;
		}
		return true;
	}

}
